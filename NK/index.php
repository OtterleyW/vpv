<h1>Tervetuloa!</h1>

<p><font color="red">Ylläpitäjän IRL-elämän tilanteen vuoksi arvonimien myöntöaika on viikosta kuukauteen. Pahoitteluni tilanteesta!</font></p>

<p>Näyttelyiden Kermaa Puoliveriset -sivusto myöntää Virtuaalisen Näyttelyjaoksen (NJ) alaisia arvonimiä niitä ansainneille puoliverisille. Aikaisemmin Näyttelyiden Kermaa -sivusto kattasi kaikki 
rodut, mutta vuonna 2007 NK jaettiin eri alajaoksiin; <a href="http://virtuaali.zafiro.ws/nk_kv/">kylmäverisiin</a>, <a href="http://virtuaali.zafiro.ws/nk_lv/">lämminverisiin</a>, 
<a href="http://cery.zafiro.ws/nk/">poneihin</a>, <a href="http://www.hoppana.net/vsr/nk/">suomen(pien)hevosiin</a> sekä <a href="http://satulapset.net/nk/">täysiverisiin</a> puoliveristen lisäksi. 
PVNK jakaa VIR MVA, Ch, Ch-M, Ch-NV ja Ch-R -arvonimiä, kuten muutkin alajaokset.</p>

<p>Puoliveristen Näyttelyiden Kermaa siirtyi sen ylläpitämisen vuonna 2008 aloittaneelta Ceryltä Diamontelle vuoden 2011 syyskuussa, häneltä Tuirelle kesäkuussa 2013 ja Tuirelta Lauralle maaliskuussa 2014. Aikaisemmin jaosta on ylläpitänyt myös cirith ja Linnea, jotka ottivat PVNK:n kontoilleen Näyttelyiden Kermaa -sivuston jakautuessa osiin toukokuussa 2007.</p>

<p>Puoliveristen Näyttelyiden Kermaa -sivuston ylläpito pyrkii parhaansa mukaan takaamaan jatkuvan toimivuuden. 
