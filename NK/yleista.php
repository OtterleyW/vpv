<h2>Säännöt</h2>
<p>Puoliveristen Näyttelyiden Kermaa -sivusto myöntää seuraavat arvonimet niitä ansainneille puoliverisille:
<ul>
<li><b>VIR MVA</b> (Virtuaalinen muotovalio)</li>
<li><b>Ch</b> (Champion)</li>
<li><b>Ch-R</b> (Champion-ruuna) </li>
<li><b>Ch-M</b> (Champion-maitovarsa) </li>
<li><b>Ch-NV</b> (Champion-nuori varsa) </li>
</ul>

<p>Hevoselle voi anoa arvonimen, jos...
<ul>
<li>Sillä on tarvittavat SERTit / maininnat vähintään kahdelta eri päätuomarilta</li>
<li>SERTit / maininnat löytyvät BIS-listalta</li>
<li>Hevosella on sivuillaan rakennekuva</li>
</ul>
</p>

<h3>Arvonimiin oikeuttavat sertit</h3>
<p><b>Virtuaalinen muotovalio (VIR MVA)</b><br />
a) 2x irtoSERT + 2x MVA-sert<br />
b) 3x MVA-sert</p>

<p><b>Champion (Ch)</b><br />
a) 3x irtoSERT<br />
b) 2x irtoSERT + 1x MVA-sert<br />
c) 1x irtoSERT + 2x MVA-sert</p>

<p><b>Champion-ruuna/maitovarsa/nuori varsa (Ch-R/Ch-M/Ch-NV)</b><br />
a) 2x näyttelyiden paras ruuna/maitovarsa/nuori varsa-maininta
</p>

<p>Arvonimeen oikeuttava SERTin/maininnan tulee olla virallinen, eli se tulee löytyä <a href="http://www.virtuaalihevoset.net/?kilpailut-ja-valmennukset/naeyttelykalenteri/bis-haku.html">BIS-listalta</a> (vanha <a href="http://virtuaali.zafiro.ws/bis/">BIS-lista</a>) ennen arvonimen anomista. Jos hevosesi arvonimeen tarvittavat SERTit/maininnat löytyvät BIS-listan haulla, niitä ei tarvitse erikseen listata arvonimihakemukseen. Jos niitä ei kuitenkaan löydy haulla, ilmoitathan ne hakemuksessa vapaavalintaisessa muodossa. Mainitsethan ainakin minkä SERTin hevosesi on saanut, milloin (päivämäärä) ja keneltä (päätuomari).
<br /><font color="blue">Haku toimii parhaiten laittamalla * nimen alkuun ja loppuun tai pelkästään jompaankumpaan!</font></p>

<h3>Hevosen kuvat</h3>
<p>Hevosen sivulta tulee löytyä vähintään yksi rakennekuva, jossa ei ole arvostelua haittaavia tekijöitä kuten loimea, satulaa tai muita isoja häiriötekijöitä (esim. ihminen arvosteltavan kohdan edessä). Pää-, liike- tai muu vastaava kuva ei siis kelpaa ja lisäksi hevosella ei saa olla kuvia useammasta eri hevosesta. 18.02.2013 lähtien samalla kuvalla voidaan myöntää arvonimi eri hevosille.</p>

