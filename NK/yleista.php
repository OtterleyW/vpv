<h1>Säännöt</h1>
<p>Puoliveristen N&auml;yttelyiden Kermaa -sivusto my&ouml;nt&auml;&auml; seuraavat arvonimet niit&auml; ansainneille puoliverisille:</p>
<p><b>VIR MVA</b> (Virtuaalinen muotovalio)<br />
<b>Ch</b> (Champion)<br />
<b>Ch-R</b> (Champion-ruuna) <br />
<b>Ch-M</b> (Champion-maitovarsa) <br />
<b>Ch-NV</b> (Champion-nuori varsa) </p>

<h2>Arvonimiin oikeuttavat sertit</h2>
<p><b>Virtuaalinen muotovalio (VIR MVA)</b><br />
a) 2x irtoSERT + 2x MVA-sert<br />
b) 3x MVA-sert</p>

<p><b>Champion (Ch)</b><br />
a) 3x irtoSERT<br />
b) 2x irtoSERT + 1x MVA-sert<br />
c) 1x irtoSERT + 2x MVA-sert</p>

<p><b>Champion-ruuna/maitovarsa/nuori varsa (Ch-R/Ch-M/Ch-NV)</b><br />
a) 2x n&auml;yttelyiden paras ruuna/maitovarsa/nuori varsa-maininta
</p>

<p>Arvonimeen oikeuttava SERTin/maininnan tulee olla virallinen, eli se tulee löytyä <a href="http://www.virtuaalihevoset.net/?kilpailut-ja-valmennukset/naeyttelykalenteri/bis-haku.html">BIS-listalta</a> (vanha <a href="http://virtuaali.zafiro.ws/bis/">BIS-lista</a>) ennen arvonimen anomista. <br />
SERTien/mainintojen tulee olla vähintään kahdelta eri päätuomarilta.<br />
<font color="blue">Haku toimii parhaiten laittamalla * nimen alkuun ja loppuun tai pelkästään jompaankumpaan!</font></p>

<h2>Hevosen kuva(t)</h2>
<p>Hevosen sivulta tulee löytyä vähintään yksi rakennekuva, jossa ei ole arvostelua haittaavia tekijöitä kuten loimea, satulaa tai muita isoja häiriötekijöitä (esim. ihminen arvosteltavan kohdan edessä). Pää-, liike- tai muu vastaava kuva ei siis kelpaa ja lisäksi hevosella ei saa olla kuvia useammasta eri hevosesta. 18.02.2013 lähtien samalla kuvalla voidaan myöntää arvonimi eri hevosille.</p>

<h2>Muistolista ja toimimattomat osoitteet?</h2>
<p><a href="muistolista.php"><b>Muistolista</b></a> oli alunperin tarkoitettu hevosille, jotka ovat kuolleet tai niiden osoite/kuva(t) eivät enää toimineet. Nykyään tätä ei kuitenkaan tehdä, vaan kaikki arvonimen saaneet hevoset pysyvät arvonimilistoilla, riippumatta toimiiko kuva(t) tai osoite.</p>

